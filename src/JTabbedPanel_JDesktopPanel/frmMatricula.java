    private void btnQuitarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnQuitarActionPerformed
        int fila, totalMatriculados=0;
        double precioTotal=0;
        //almacena la fila que se selecciono en el JTable
        fila = tablaResumen.getSelectedRow();
        //si hay una fila seleccionada
        if(fila>=0){
            //eliminamos la fila seleccionada
            dtm.removeRow(fila);
            
            //obtenemos la cantidad de filas que aún hay
            totalMatriculados = dtm.getRowCount();
            //sumamos nuevamente el precio de cada uno de las filas
            for(int i=0; i<totalMatriculados; i++){
                precioTotal = precioTotal + Double.parseDouble((String)
                            tablaResumen.getValueAt(i, 4));
                //getValueAt: el valor que hay en esa posición
            }
            //imprimimos el precio total en el cuadro txt
            txtPrecioTotal.setText(String.valueOf(precioTotal));
        } else {
            JOptionPane.showMessageDialog(null,"Selecciona una fila de la Tabla"
                    + "para Eliminar");
        }
    }//GEN-LAST:event_btnQuitarActionPerformed

