    private void btnMatricularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMatricularActionPerformed
        String datos[] = new String[5];
        int totalMatriculados, costo; //costo del curso y se elige type pago
        double precioTotal=0;
        //almacena el contenido del cuadro txt
        costo = Integer.parseInt(txtCosto.getText());
        
        //asignamos el valor selccionado del ComboBox
        datos[0] = (String)cboCurso.getSelectedItem();
        //asignamos el contenido de los cuadros txt
        datos[1] = txtDuracion.getText();
        datos[2] = txtCosto.getText();
        
        //si ha seleccionado contado, sino En cuotas
        if (rbContado.isSelected()){
            datos[3] = "Contado";
            datos[4] = String.valueOf(costo*0.95); //descuentado 5%
        } else {
            datos[3] = "Pago en cuotas";
            datos[4] = String.valueOf(costo*1.1); //incrementado 10%
        }
        //agregamos la fila al objeto JTable con los datos en el arreglo datos
        dtm.addRow(datos);
        
        //contamos la cantidad de filas, ósea la cantidad de cursos matriculados
        totalMatriculados = dtm.getRowCount();
        /*con este bucle obtendremos el precio total, ósea sumamos cada fila 
                                                           de la columna 5*/
        for(int i=0; i<totalMatriculados; i++){
            precioTotal = precioTotal + Double.parseDouble((String)
                                        tablaResumen.getValueAt(i, 4));
        }
        //imprimimos el valor obtenido en el siguiente cuadro txt
        txtPrecioTotal.setText(String.valueOf(precioTotal));
        
        //limpiamos/inicializamos para que pueda seleccionar otro curso
        cboCurso.setSelectedIndex(0);
        txtDuracion.setText("");
        txtCosto.setText("");
    }//GEN-LAST:event_btnMatricularActionPerformed

