    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        double bonificacion= 0.0, sueldoBasico, descuento, sueldoNeto, sueldoBruto;
        
        sueldoBasico = Double.parseDouble(txtSueldoBasico.getText());
        
        //Grado de instruccion
        if(rbSecundaria.isSelected())
            bonificacion = sueldoBasico*0.05;
        if(rbUniversitaria.isSelected())
            bonificacion = sueldoBasico*0.12;
        if(rbMaestria.isSelected())
            bonificacion = sueldoBasico*0.18;
        //Condici√≥n laboral
        if(rbContratado.isSelected())
            bonificacion = bonificacion+sueldoBasico*0.04;
        if(rbNombrado.isSelected())
            bonificacion = bonificacion+sueldoBasico*0.01;
        if(rbSNP.isSelected())
            bonificacion = bonificacion+sueldoBasico*0.05;
        
        if(!(chkCasado.isSelected()))
            bonificacion = bonificacion+sueldoBasico*0.03;
        if(chkHijos.isSelected())
            bonificacion = bonificacion+sueldoBasico*0.06;
        
        sueldoBruto = sueldoBasico+bonificacion;
        descuento = sueldoBruto*0.11;
        sueldoNeto = sueldoBruto - descuento;
        
        txtBonificacion.setText(String.valueOf(bonificacion));
        txtDescuento.setText(String.valueOf(descuento));
        txtSueldoNeto.setText(String.valueOf(sueldoNeto));
    }//GEN-LAST:event_btnCalcularActionPerformed

